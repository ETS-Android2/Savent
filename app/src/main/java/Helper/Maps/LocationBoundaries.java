package Helper.Maps;


/**
 * Class that allow you to define a square area into the maps coordinate system
 */
public class LocationBoundaries{

    //Higher latitude, the one further north
    public double upperLatitude = 0;

    //Lower latitude, the one less to the north
    public double lowerLatitude = 0;

    //Longitude further west
    public double leftLongitude = 0;

    //Longitude further east
    public double rightLongitude = 0;

    public LocationBoundaries(){ }
}

